#include <stdio.h>

int	ft_sqrt(int nb)
{
	long	i;
	long	b;

	i = 1;
	b = nb;
	if (b <= 0)
		return (0);
	if (b == 1)
		return (1);
	if (b >= 2)
	{
		while ((i * i) <= b)
		{
			if ((i * i) == b)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}

